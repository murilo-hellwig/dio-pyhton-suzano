#exemplos de conversão de tipos de dados

#conversão de inteiro para float
preco = 10
preco_float = float(preco)

#conversão de float para inteiro
preco_inteiro = int(preco_float)

#convesão por divisão:
preco = 39
preco_float = preco / 5  # resultado será um float
print(preco_float)  # neste caso ele mantém a parte decimal
print(preco // 5) #neste caso ele faz a divisão e elimia o que tiver depois da vírgula

