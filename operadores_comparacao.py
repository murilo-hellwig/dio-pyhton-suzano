#testes comparação de valores entre variáveis. O retorno sempre é booleano.
# == para verificar se são iguais  
# != para verificar se são diferentes
# > para verificar se é maior
# < para verificar se é menor
# >= para verificar se é maior ou igual
# <= para verificar se é menor ou igual
# Exemplo de comparação
a = 10
b = 20
print(a == b)  # False
print(a != b)  # True   
print(a > b)   # False
print(a < b)   # True   
print(a >= b)  # False
print(a <= b)  # True   
# Exemplo de comparação com strings
nome1 = "Ana"
nome2 = "Maria"
print(nome1 == nome2)  # False
print(nome1 != nome2)  # True

# Exemplo de comparação com listas
lista1 = [1, 2, 3]
lista2 = [1, 2, 3]
print(lista1 == lista2)  # True
print(lista1 != lista2)  # False

